package com.callor.service;

public class Line {
	public static void dLine(int length) {
		System.out.println("=".repeat(length));
	}

	public static void sLine(int legnth) {
		System.out.println("-".repeat(legnth));

	}

	public static void bLine(int legnth) {
		System.out.println("+-".repeat(legnth));

	}
}